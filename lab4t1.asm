CODE SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CODE, DS:CODE
	ORG 100H
	
START: JMP ENTRY

SIR		DB 1, 2, 3, 4
LGSIR	DB $-SIR
SUM		DB 0

ENTRY:
	MOV		CH, 0
	MOV		CL, LGSIR
	MOV 	AL, 0
	MOV		SI, 0
	
NEXT:
	ADD		AL, SIR[SI]
	INC 	SI
	LOOP	NEXT
	MOV		SUM, AL
	
	MOV		AX, 4C00H
	INT		21H

CODE ENDS
END START