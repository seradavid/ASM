DATA SEGMENT PARA PUBLIC 'DATA'
	SIR		DW		1, 2, 3, 4
	LGSIR	DB		($-SIR) / 2
	SUM		DD		0
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA
START	PROC FAR
	PUSH 	DS
	XOR		AX, AX
	PUSH 	AX
	MOV 	AX, DATA
	MOV 	DS, AX
	
	MOV		CH, 0
	MOV		CL, LGSIR
	MOV 	AX, 0
	MOV		BX, 0
	MOV 	SI, 0
	
	NEXT:
		ADD		AX, SIR[SI]
		ADC		BX, 0
		ADD		SI, 2
		LOOP 	NEXT
	MOV		WORD PTR SUM[0], AX
	MOV 	WORD PTR SUM[2], BX
	
	RET
START	ENDP
CODE ENDS
END START