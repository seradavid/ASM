DATA1 SEGMENT PARA PUBLIC 'DATA'
	SIR1	DB	'123456'
	LEN1	DB	$-SIR1
DATA1 ENDS

DATA2 SEGMENT PARA PUBLIC 'DATA'
	SIR2	DB	6 DUP(?)
	LEN2	DB	$-SIR2
DATA2 ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
	START PROC FAR
	ASSUME	 CS:CODE, DS:DATA1, ES:DATA2
	
	PUSH	DS
	XOR		AX, AX
	PUSH 	AX
	MOV		AX, DATA1
	MOV		DS, AX
	
	MOV		AX, DATA2
	MOV		ES, AX
	
	XOR		AX, AX
	XOR		CX, CX
	
	MOV		SI, 0
	MOV		DI, 0
	MOV		CL, LEN1
	
	START_LOOP:
		MOV		AL, SIR1[SI]
		MOV		SIR2[DI], AL
		INC		SI
		INC 	DI
		LOOP	START_LOOP
	
	RET
	
	START ENDP
CODE ENDS
END START